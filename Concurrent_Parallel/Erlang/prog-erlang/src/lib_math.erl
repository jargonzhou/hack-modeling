-module(lib_math).

%% ====================================================================
%% API functions
%% ====================================================================
-export([fac/1,
  fib/1,
  max/2]).

fac(0) -> 1;
fac(N) -> N * fac(N - 1).

fib(1) -> 1;
fib(2) -> 1;
fib(N) -> fib(N - 1) + fib(N - 2).

max(X, Y) when X > Y -> X; % 使用守卫(guard)
max(_X, Y) -> Y.

