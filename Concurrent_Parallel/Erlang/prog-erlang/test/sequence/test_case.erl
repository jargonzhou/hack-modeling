-module(test_case).
-include("common.hrl").

%% ====================================================================
%% API functions
%% ====================================================================
-export([run/0]).

run() ->
  ?INFO("filter(fun X -> X > 2 end, [1,2,3,4])",
    filter(fun(X) -> X > 2 end, [1, 2, 3, 4])).

%% ====================================================================
%% Internal functions
%% ====================================================================

filter(P, [H | T]) ->
  case P(H) of
    true -> [H | filter(P, T)];
    false -> filter(P, T)
  end;
filter(_P, []) ->
  [].
		
