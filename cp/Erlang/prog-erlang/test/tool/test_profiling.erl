-module(test_profiling).
-include("common.hrl").

%% ====================================================================
%% API functions
%% ====================================================================
-export([test_timer/0,
  calc/1]).

test_timer() ->
  % 计时
  {M, _} = timer:tc(?MODULE, calc, [10]),
  ?INFO("In microseconds", M).

calc(N) ->
  F = fun(_) -> 123456789 * 987654321 end,
  L = lib_misc:for(1, N, F),
  ?INFO("L", L).

