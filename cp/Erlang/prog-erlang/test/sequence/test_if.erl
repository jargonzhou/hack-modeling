-module(test_if).

%% ====================================================================
%% API functions
%% ====================================================================
-export([run/0]).

run() -> test_if().

%% ====================================================================
%% Internal functions
%% ====================================================================

%% >1
%% ok
test_if() ->
  X = 2,
  if
    X > 1 -> io:format(">1~n");
    X < 2 -> io:format("<2~n");
    true -> io:format("ok~n")
  end.
	

